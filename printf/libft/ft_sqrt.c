double	ft_sqrt(double x)
{
	int i;

	i = 1;
	if (x == -0)
		return (-0);
	if (x < 0 || x == 0)
		return (0);
	if (x == 2)
		return (0);
	while ((i * i) < x)
		i++;
	if ((i * i) == x)
		return (i);
	return (0);
}
